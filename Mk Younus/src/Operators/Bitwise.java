package Operators;

public class Bitwise {

	public static void main(String[] args) {
		int x = 0b01010;
		int y = 0b0110;
		int z;
		z = x & y;
		System.out.println(z);
		z = x | y;
		System.out.println(z);
		int c;
		c = x << 1;
		System.out.println(c);
	int g=29;
	System.out.println(String.format(Integer.toBinaryString(g)));

	}

}
