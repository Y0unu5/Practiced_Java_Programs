package Loops;

public class While_DoWhile {

	public static void main(String[] args) {
		// Demonstration While Loop
		int i = 1;
		while (i < 100) {

			System.out.println(i);
			i = i * 2;
		}
		//Do While Loop
		int b = 1;
		do {
			System.out.println(b);
			b = b * 2;
		} while (b < 200);
	}

}
